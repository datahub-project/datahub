from typing import Dict, List
import json
import pathlib
import os
import shutil
import hashlib

current_dir = pathlib.Path(__file__).parent.resolve()
print("eval directory", current_dir)
print("parent directory", current_dir.parent)


# Create a .env file in eval directory
# Set env variables BEDROCK_AWS_ACCESS_KEY_ID, BEDROCK_AWS_SECRET_ACCESS_KEY,BEDROCK_AWS_REGION
import dotenv

dotenv.load_dotenv()


from datahub.utilities.perf_timer import PerfTimer

from datahub_integrations.gen_ai.description_v2 import (
    extract_metadata_for_urn,
)

from graph_helper import create_datahub_graph


urns_dict: Dict[str, List[str]] = json.loads(
    (current_dir / "eval_urns.json").read_text()
)
print(urns_dict.keys())
print(sum(len(urns_list) for urns_list in urns_dict.values()))


# Copy graph_credentials.json file to parent docs_generation directory
# generated using https://github.com/acryldata/experimental/blob/main/hsheth/bulk-graph-creds/generate_many_graph_credentials.py


# WARNING: This cell takes a long time (4 minutes) to run and will delete existing eval data
# Only run this cell if you intend to regenerate all eval data


eval_data_path = current_dir / "eval_data"
print("eval data path", eval_data_path)
shutil.rmtree(eval_data_path, ignore_errors=True)
os.makedirs(eval_data_path)

for deployment, urns_list in urns_dict.items():
    graph_client = create_datahub_graph(deployment)
    for idx, urn_obj in enumerate(urns_list):
        if urn_obj.get("skip", False):
            continue
        urn = urn_obj["urn"]
        with PerfTimer() as timer:
            print(f"Processing {deployment} URN: {urn}...")
            entity = graph_client.get_entity_semityped(urn)
            # Remove edited description from entity as that could have been generated by AI
            # via "Generate Description" button in DataHub UI
            if entity.get("editableDatasetProperties"):
                entity["editableDatasetProperties"].description = None

            extracted_entity_info = extract_metadata_for_urn(
                entity, urn, graph_client
            ).dict()
            extraction_seconds = timer.elapsed_seconds()
            eval_entry = {
                "urn": urn,
                "aspects_list": list(entity.keys()),
                "extracted_entity_info": extracted_entity_info,
                "deployment": deployment,
                "category": urn_obj["category"],
                "extraction_seconds": extraction_seconds,
            }
            urn_hash = hashlib.md5(urn.encode("utf-8")).hexdigest()

        # write eval_entry to a json file
        with open(eval_data_path / f"{deployment}_{urn_hash}.json", "w") as f:
            json.dump(eval_entry, f, indent=2)


print(len(os.listdir(eval_data_path)), "eval entries generated")
