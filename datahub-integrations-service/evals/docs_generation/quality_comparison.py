#!/usr/bin/env python3
"""
Quality comparison module for evaluating AI-generated dataset descriptions.

Uses Bedrock Opus 4.5 to compare the quality of descriptions generated by different models.
"""

import asyncio
import json
import logging
import traceback
from typing import Dict, List, Optional

import boto3


async def compare_descriptions_quality(
    baseline_desc: str,
    eval_desc: str,
    urn: str,
    baseline_col_desc: Optional[Dict[str, str]] = None,
    eval_col_desc: Optional[Dict[str, str]] = None,
    bedrock_region: str = "us-west-2",
) -> Dict:
    """
    Use Bedrock Opus to compare quality of two descriptions.

    Args:
        baseline_desc: Description from baseline model
        eval_desc: Description from eval model
        urn: Dataset URN for context
        baseline_col_desc: Column descriptions from baseline model
        eval_col_desc: Column descriptions from eval model
        bedrock_region: AWS region for Bedrock (default: us-west-2)

    Returns:
        Dictionary with quality comparison results
    """
    if not baseline_desc or not eval_desc:
        return {
            "comparison": "N/A - one or both descriptions missing",
            "winner": None,
            "reasoning": "Cannot compare missing descriptions",
        }

    # Build column descriptions section
    baseline_col_section = ""
    if baseline_col_desc:
        baseline_col_section = "\n\nColumn Descriptions:\n" + "\n".join(
            f"- {col}: {desc}" for col, desc in baseline_col_desc.items()
        )

    eval_col_section = ""
    if eval_col_desc:
        eval_col_section = "\n\nColumn Descriptions:\n" + "\n".join(
            f"- {col}: {desc}" for col, desc in eval_col_desc.items()
        )

    prompt = f"""You are evaluating the quality of two AI-generated dataset descriptions. Compare them based on:
1. Accuracy and relevance to the dataset URN: {urn}
2. Clarity and readability
3. Completeness and informativeness (including column-level descriptions)
4. Professional tone and grammar

Description A (Baseline):
{baseline_desc}{baseline_col_section}

Description B (Eval):
{eval_desc}{eval_col_section}

Provide your analysis in JSON format with these fields:
- winner: "A", "B", or "tie"
- reasoning: Brief explanation (1-2 sentences)
- quality_score_a: Integer score 1-10
- quality_score_b: Integer score 1-10

Respond with ONLY the JSON object, no additional text."""

    try:
        # Initialize Bedrock client
        bedrock_runtime = boto3.client(
            service_name="bedrock-runtime", region_name=bedrock_region
        )

        # Prepare request for Claude Opus 4.5
        request_body = {
            "anthropic_version": "bedrock-2023-05-31",
            "max_tokens": 1000,
            "messages": [{"role": "user", "content": prompt}],
        }

        # Call Bedrock
        loop = asyncio.get_event_loop()
        response = await loop.run_in_executor(
            None,
            lambda: bedrock_runtime.invoke_model(
                modelId="us.anthropic.claude-opus-4-5-20251101-v1:0",
                body=json.dumps(request_body),
            ),
        )

        # Parse response
        response_body = json.loads(response["body"].read())
        content = response_body["content"][0]["text"]

        # Extract JSON from response (handle markdown code blocks)
        content = content.strip()
        if content.startswith("```"):
            # Remove markdown code block markers
            lines = content.split("\n")
            content = "\n".join(lines[1:-1]) if len(lines) > 2 else content

        result = json.loads(content)

        return {
            "winner": result.get("winner"),
            "reasoning": result.get("reasoning"),
            "quality_score_baseline": result.get("quality_score_a"),
            "quality_score_eval": result.get("quality_score_b"),
            "comparison": "completed",
        }

    except Exception as e:
        error_traceback = traceback.format_exc()
        logging.error(f"Error comparing descriptions for {urn}: {error_traceback}")
        return {
            "comparison": "error",
            "winner": None,
            "reasoning": f"Error during comparison: {str(e)}",
            "error": str(e),
            "error_traceback": error_traceback,
        }


async def run_quality_comparisons(
    baseline_results: List[Dict],
    eval_results: List[Dict],
    bedrock_region: str = "us-west-2",
    concurrency: int = 5,
) -> List[Dict]:
    """
    Run quality comparisons for all baseline/eval pairs.

    Args:
        baseline_results: Results from baseline run
        eval_results: Results from eval run
        bedrock_region: AWS region for Bedrock
        concurrency: Number of concurrent comparison requests

    Returns:
        List of comparison results
    """
    # Create mapping of URN to results
    baseline_by_urn = {r["urn"]: r for r in baseline_results}
    eval_by_urn = {r["urn"]: r for r in eval_results}

    # Get URNs that have both baseline and eval descriptions
    common_urns = set(baseline_by_urn.keys()) & set(eval_by_urn.keys())
    comparable_urns = [
        urn
        for urn in common_urns
        if baseline_by_urn[urn].get("table_description")
        and eval_by_urn[urn].get("table_description")
    ]

    print(f"\n{'=' * 80}")
    print(
        f"QUALITY COMPARISON: Comparing {len(comparable_urns)} description pairs using Opus 4.5"
    )
    print(f"{'=' * 80}")

    if not comparable_urns:
        print(
            "No comparable descriptions found (both baseline and eval must have table descriptions)"
        )
        return []

    # Create semaphore to limit concurrency
    semaphore = asyncio.Semaphore(concurrency)

    async def compare_with_semaphore(urn: str, idx: int) -> Dict:
        async with semaphore:
            print(f"[{idx}/{len(comparable_urns)}] Comparing descriptions for: {urn}")

            baseline_desc = baseline_by_urn[urn]["table_description"]
            eval_desc = eval_by_urn[urn]["table_description"]
            baseline_col_desc = baseline_by_urn[urn].get("column_descriptions")
            eval_col_desc = eval_by_urn[urn].get("column_descriptions")

            comparison = await compare_descriptions_quality(
                baseline_desc,
                eval_desc,
                urn,
                baseline_col_desc,
                eval_col_desc,
                bedrock_region,
            )

            winner = comparison.get("winner", "N/A")
            reasoning = comparison.get("reasoning", "N/A")
            baseline_score = comparison.get("quality_score_baseline", "N/A")
            eval_score = comparison.get("quality_score_eval", "N/A")

            print(
                f"  Winner: {winner} (Baseline: {baseline_score}/10, Eval: {eval_score}/10)"
            )
            print(f"  Reasoning: {reasoning}")

            return {"urn": urn, **comparison}

    # Run comparisons concurrently
    tasks = [
        compare_with_semaphore(urn, idx) for idx, urn in enumerate(comparable_urns, 1)
    ]
    comparisons = await asyncio.gather(*tasks)

    # Print summary
    winners = [c["winner"] for c in comparisons if c.get("winner")]
    baseline_wins = winners.count("A")
    eval_wins = winners.count("B")
    ties = winners.count("tie")

    print(f"\n{'=' * 80}")
    print("QUALITY COMPARISON SUMMARY")
    print(f"{'=' * 80}")
    print(f"Total comparisons: {len(comparisons)}")
    print(f"Baseline wins: {baseline_wins}")
    print(f"Eval wins: {eval_wins}")
    print(f"Ties: {ties}")

    if winners:
        avg_baseline_score = sum(
            c.get("quality_score_baseline", 0) for c in comparisons
        ) / len(comparisons)
        avg_eval_score = sum(c.get("quality_score_eval", 0) for c in comparisons) / len(
            comparisons
        )
        print(f"Avg baseline quality score: {avg_baseline_score:.2f}/10")
        print(f"Avg eval quality score: {avg_eval_score:.2f}/10")

    print(f"{'=' * 80}\n")

    return comparisons
