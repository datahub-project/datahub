// Curated set of Phosphor icons meaningful for representing a *domain* — a business
// area, function, or topic. Deliberately excludes:
//   - UI chrome (arrows, carets, alignment, brand logos, cursors, etc.)
//   - Icons already reserved for platform entity types (Database → DataPlatform,
//     Table → Dataset, ChartBar → Dashboard, ChartLine → Chart, Folder → Container,
//     Globe → Domain default, BookmarkSimple / BookmarksSimple → Glossary, etc.)
//   - Icons used prominently in the left nav / entity sidebar / entity profile tabs
//     / action menus / settings / search / core alchemy components — see the
//     "reserved" inventory in this file's PR for the canonical list. Reusing those
//     icons in a domain picker would visually collide with existing product semantics.
//
// Icons are imported statically so opening the modal loads them in a SINGLE picker
// chunk instead of firing N concurrent `React.lazy` chunk requests, which
// previously overwhelmed the dev server and left cells stuck in the Suspense fallback.
//
// Ordered by category to give a scannable layout in the flat picker grid. Search is
// case-insensitive substring match against the icon name.
//
// A domain that stored a name not in this list (e.g. a Phosphor name previously in this
// library) still renders correctly on the read side via `hasLazyIcon` + `getLazyIcon` in
// DomainColoredIcon. The picker also "pins" the currently-stored icon at the top so it's
// always visible while editing, even if it's no longer part of the curated set.
//
// Legacy MUI icon names on existing domains can be rewritten to their Phosphor equivalents
// with the operator script at
// `metadata-ingestion/examples/library/domain_migrate_mui_icons_to_phosphor.py`.

