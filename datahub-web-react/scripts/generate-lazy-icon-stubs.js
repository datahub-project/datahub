#!/usr/bin/env node
// Generates a thin re-export stub for every Phosphor CSR icon into lazy-icons/.
// Vite builds each stub as a separate async chunk so the main bundle stays icon-free —
// end users only download the specific icons an admin configured, not all 1500+.
// Also emits lazyIconNames.ts — a side-effect-free name index that lets main-bundle
// consumers check icon-name validity without pulling the full glob map.
// Run via the Gradle task generateLazyIconStubs, or: node scripts/generate-lazy-icon-stubs.js

const fs = require('fs');
const path = require('path');

const csrDir = path.resolve(__dirname, '../node_modules/@phosphor-icons/react/dist/csr');
const stubsDir = path.resolve(__dirname, '../src/app/mfeframework/lazy-icons');
const namesFile = path.resolve(__dirname, '../src/app/mfeframework/lazyIconNames.ts');

const iconNames = fs
    .readdirSync(csrDir)
    .filter((f) => f.endsWith('.mjs'))
    .map((f) => f.slice(0, -4)) // strip .mjs
    .sort();

fs.mkdirSync(stubsDir, { recursive: true });

for (const name of iconNames) {
    fs.writeFileSync(
        path.join(stubsDir, `${name}.ts`),
        `export { ${name} } from '@phosphor-icons/react/dist/csr/${name}';\n`,
    );
}

// Emit a plain-string names file. This is imported by main-bundle code (e.g. DomainColoredIcon)
// to check whether a stored icon name resolves to a real Phosphor stub. Kept in a dedicated
// module so consumers don't drag in the iconLoader's glob map (~1,500 dynamic-import closures).
const namesLiteral = iconNames.map((n) => `    '${n}'`).join(',\n');
fs.writeFileSync(
    namesFile,
    `// AUTO-GENERATED by scripts/generate-lazy-icon-stubs.js — do not edit by hand.\n` +
        `// Regenerate via \`./gradlew :datahub-web-react:generateLazyIconStubs\` or\n` +
        `// \`node datahub-web-react/scripts/generate-lazy-icon-stubs.js\`.\n\n` +
        `const LAZY_ICON_NAMES_SET: ReadonlySet<string> = new Set([\n${namesLiteral},\n]);\n\n` +
        `export function hasLazyIcon(name: string | null | undefined): boolean {\n` +
        `    return !!name && LAZY_ICON_NAMES_SET.has(name);\n` +
        `}\n\n` +
        `export const LAZY_ICON_NAMES: readonly string[] = Array.from(LAZY_ICON_NAMES_SET);\n`,
);

console.log(
    `[generate-lazy-icon-stubs] ${iconNames.length} stubs → ${path.relative(process.cwd(), stubsDir)}\n` +
        `[generate-lazy-icon-stubs] names index → ${path.relative(process.cwd(), namesFile)}`,
);
