#!/usr/bin/env bash
# test-docs-links.sh — Run the full docs generation pipeline locally and
# catch broken links before pushing to CI.
#
# Usage:
#   cd docs-website
#   bash scripts/test-docs-links.sh
#
# What it does:
#   1. Runs docgen.py to regenerate source docs (docs/generated/ingestion/sources/)
#   2. Runs generateDocsDir.ts which validates all GitHub repo links
#   3. Reports ALL broken links found (not just the first one, unlike CI)
#
# Requirements:
#   - metadata-ingestion venv must be set up (../gradlew :metadata-ingestion:installDev)
#   - Node/yarn must be available (run `../gradlew yarnInstall` once)

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
DOCS_WEBSITE="$REPO_ROOT/docs-website"
INGESTION="$REPO_ROOT/metadata-ingestion"

echo "=== Step 1: Regenerating source docs via docgen.py ==="
cd "$INGESTION"
source venv/bin/activate
python scripts/docgen.py \
  --out-dir ../docs/generated/ingestion \
  --connector-registry-dir src/datahub/ingestion/autogenerated \
  --extra-docs "$(pwd)/docs/sources" \
  2>&1 | grep -E "^(INFO|WARNING|ERROR|Traceback|ValueError|Ingestion)" | tail -20
echo ""

echo "=== Step 2: Running generateDocsDir.ts link validation ==="
cd "$DOCS_WEBSITE"

# Patch generateDocsDir.ts to collect ALL errors instead of throwing on the first
python3 - << 'PATCH'
import re

with open("generateDocsDir.ts") as f:
    content = f.read()

# Replace the single throw with a collected-error approach
old = '''      throw new Error(
        `broken github repo link to ${updated_path} in ${filepath}`
      );'''
new = '''      broken_links.push(`  ${filepath}: ${updated_path}`);'''

if old in content:
    # Add broken_links array at top of main() and report at end
    content = content.replace(
        'async function main() {',
        'const broken_links: string[] = [];\nasync function main() {'
    )
    content = content.replace(old, new)
    # Add reporting before the final resolve
    content = content.replace(
        '  console.log("Done.");',
        '''  if (broken_links.length > 0) {
    console.error("\\n=== BROKEN GITHUB REPO LINKS (" + broken_links.length + ") ===");
    broken_links.forEach(l => console.error(l));
    process.exit(1);
  }
  console.log("Done. No broken links found.");'''
    )
    with open("generateDocsDir.ts", "w") as f:
        f.write(content)
    print("Patched generateDocsDir.ts to collect all errors")
else:
    print("Pattern not found — generateDocsDir.ts may already be patched or changed")
PATCH

# Run the script, capturing all errors
EXIT=0
yarn _generate-docs-dir-script 2>&1 || EXIT=$?

# Revert the patch
git checkout generateDocsDir.ts 2>/dev/null || true

if [ $EXIT -ne 0 ]; then
  echo ""
  echo "=== FAILED: Fix the broken links above before pushing ==="
  exit 1
else
  echo ""
  echo "=== PASSED: No broken links found ==="
fi
