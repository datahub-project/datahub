#!/bin/bash
set -euo pipefail

OUTDIR=./custom-package
PACKAGE_NAME="acryl-datahub-cloud"
PACKAGE_VERSION="1!0.0.0.dev0"

# Note: this assumes that datahub has already been built with `./gradlew build`.
DATAHUB_ROOT=../..

SCHEMAS_PDL="$DATAHUB_ROOT/metadata-models/src/main/pegasus/com/linkedin"
SCHEMAS_AVSC="$DATAHUB_ROOT/metadata-events/mxe-schemas/src/renamed/avro/com/linkedin"
ENTITY_REGISTRY="$DATAHUB_ROOT/metadata-models/src/main/resources/entity-registry.yml"

rm -r $OUTDIR 2>/dev/null || true
python $DATAHUB_ROOT/metadata-ingestion/scripts/custom_package_codegen.py $ENTITY_REGISTRY $SCHEMAS_PDL $SCHEMAS_AVSC $OUTDIR "$PACKAGE_NAME" "$PACKAGE_VERSION" --no-build

# Copy files from the generated package.
echo 'Moving in the autogenerated files'
cp $OUTDIR/acryl-datahub-cloud/src/acryl_datahub_cloud/_codegen_config.json ./src/acryl_datahub_cloud/
rm -r ./src/acryl_datahub_cloud/metadata 2>/dev/null || true
cp -r $OUTDIR/acryl-datahub-cloud/src/acryl_datahub_cloud/metadata ./src/acryl_datahub_cloud/metadata
rm -r $OUTDIR
